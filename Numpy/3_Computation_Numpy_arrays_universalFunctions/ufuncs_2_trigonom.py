
import numpy as np


a = np.linspace(0,  np.pi, 3)   # angle
x = np.array([-1, 0, 1])        # value


print("\n")
print("----------------------------------")

print("a = ", a)
print("sin(a) = ", np.sin(a) )
print("cos(a) = ", np.cos(a) )
print("tan(a) = ", np.tan(a) )

print("\n")

print("x = ", x)
print("arcsin(x) = ", np.arcsin(x) )
print("arccos(x) = ", np.arccos(x) )
print("arctan(x) = ", np.arctan(x) )


print("----------------------------------")
print("\n")



